let users = []

// Adiciona um item ao final do Array.
users.push("Erika")
users.push("Edson")
users.push("Elena")


// Adiciona um item no início do Array.
users.unshift("Ana")
console.log(users)


// Remove o item do início do Array.
users.shift()
console.log(users)


// Remove do final do Array.
users.pop()
console.log(users)