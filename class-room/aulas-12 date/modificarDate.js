let date = new Date("july 3, 2025 14:30:00")

// Modificar o ano.
date.setFullYear(2030)

// Modificar o mês (começa com 0).
date.setMonth(7)

// Modificar o dia.
date. setDate(26)

// Modificar a hora.
date.setHours(21)

// Modificar o minuto.
date.setMinutes(50)

// Modificar o segundo.

date.setSeconds(44)
